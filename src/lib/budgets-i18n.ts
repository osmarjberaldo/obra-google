// Registra/atualiza o namespace para os idiomas suportados
export function ensureBudgetsI18n() {
  const langs = Object.keys(resources);
  for (const lang of langs) {
    const bundle = (resources as any)[lang];
    // usa deep merge e overwrite=true para garantir atualização de chaves
    i18n.addResourceBundle(lang, BUDGETS_NS, bundle, true, true);
  }
}

export default resources;